    public final void showDataCBo(){
        List<String> lst = controller.getLoaiSP();
        cboLoaiSP.addItem("ALL");
        for(int i = 0 ; i < lst.size() ; i++){
            cboLoaiSP.addItem(lst.get(i));
        }
    }
    
    public final void showDataTable() {
        clearData();
        List<SanPham> lst = controller.getAll();
        for(int i = 0 ; i < lst.size() ; i++){
            Object[] row = {
                lst.get(i).getMaSP(),
                lst.get(i).getTenSP(),
                lst.get(i).getDonGia(),
                lst.get(i).getMaLoai()
            };
            tableModel.addRow(row);
        }
    }
    
    public final void clearData(){
        int n = tableModel.getRowCount() - 1;
        for (int i = n; i >= 0; i--) {
            tableModel.removeRow(i);
        }
    }
    
    public final void clearText(){
        txtGia.setText("");
        txtLoaiSP.setText("");
        txtMaSP.setText("");
        txtTenSP.setText("");
    }

