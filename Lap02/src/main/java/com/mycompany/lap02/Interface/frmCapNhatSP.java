    private void tbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSanPhamMouseClicked
        // TODO add your handling code here:
        TableModel tableModel = tbSanPham.getModel();
        int row = tbSanPham.getSelectedRow();
        String MaSP =  tableModel.getValueAt(row, 0).toString();
        SanPham sp = controller.getSanPham(MaSP);
        txtMaSP.setText(sp.getMaSP());
        txtTenSP.setText(sp.getTenSP());
        txtGia.setText(String.valueOf(sp.getDonGia()));
        txtLoaiSP.setText(sp.getMaLoai());
        String ml = controller.getMaLoai(sp.getMaLoai());
        cboLoaiSP.setSelectedItem(ml);
    }//GEN-LAST:event_tbSanPhamMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String MaSP = txtMaSP.getText();
        String TenLoai = txtTenSP.getText();
        float DonGia = Float.parseFloat(txtGia.getText());
        String MaLoai = (String)cboLoaiSP.getSelectedItem();
        SanPham sp = new SanPham(MaSP, TenLoai, DonGia, MaLoai);
        try {
            boolean them = controller.insertData(sp);
            if(them){
                JOptionPane.showMessageDialog(rootPane, "Them thanh cong");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Them khong thanh cong");
            }
            showDataTable();
            clearText();
        } catch (SQLException ex) {
            Logger.getLogger(frmCapNhatSP.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

